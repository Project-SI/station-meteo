output () {
  echo -e "* ${1}"
}

error() {
  COLOR_RED='\033[0;31m'
  COLOR_NC='\033[0m'

  echo ""
  echo -e "* ${COLOR_RED}ERROR${COLOR_NC}: $1"
  echo ""
}
python() {
    sudo apt install python3
}

while [ "$done" == false ]; do
  options=(
    "Install python 3"
    "Install node-red"
    "update system"
  )

  actions=(
    "python"
    "nodered"
    "update"
  )
  output " "que voulez faire ?"

  for i in "${!options[@]}"; do
    output " "[$i] ${options[$i]}"
  done

  echo -n "* Input 0-$((${#actions[@]}-1)): "
  read -r action

  [ -z "$action" ] && error "une entrée est attendu " && continue

  valid_input=("$(for ((i=0;i<=${#actions[@]}-1;i+=1)); do echo "${i}"; done)")
  [[ ! " ${valid_input[*]} " =~ ${action} ]] && error "option invalide"
  [[ " ${valid_input[*]} " =~ ${action} ]] && done=true && eval "${actions[$action]}"
done
